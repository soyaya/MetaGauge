/**
 * FinancialPDFGenerator
 * Generates investor-grade PDF financial reports using pdfkit.
 * Standard data room format: clean, minimal, professional.
 */
import PDFDocument from 'pdfkit';
import { createWriteStream, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_DIR = join(__dirname, '../../reports/financial');

// ── Brand colours ──────────────────────────────────────────────────────────
const C = {
  black:    '#0a0a0a',
  white:    '#ffffff',
  accent:   '#1a1a2e',    // dark navy — professional data room feel
  mid:      '#4a5568',    // body text
  light:    '#e2e8f0',    // table borders / dividers
  pos:      '#22543d',    // positive numbers (green)
  neg:      '#742a2a',    // negative numbers (red)
  posLight: '#f0fff4',
  negLight: '#fff5f5',
  pageW:    595,          // A4 width in points
  pageH:    842,          // A4 height in points
  marginX:  56,
  marginY:  56,
};

const bodyW = C.pageW - C.marginX * 2;

// ── Helpers ────────────────────────────────────────────────────────────────

function usd(n) {
  if (n == null || isNaN(n)) return '$0.00';
  const abs = Math.abs(n);
  const str = abs.toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
  return n < 0 ? `($${str})` : `$${str}`;
}

function pct(n) {
  if (n == null || isNaN(n)) return '0%';
  return `${Number(n).toFixed(1)}%`;
}

function ensureDir(filePath) {
  mkdirSync(dirname(filePath), { recursive: true });
}

// ── PDF builder class ──────────────────────────────────────────────────────

class PDFBuilder {
  constructor(doc) {
    this.doc = doc;
    this.y = C.marginY;
  }

  // Move to next line
  nl(h = 14) { this.y += h; }

  // Draw a horizontal rule
  hr(color = C.light, thickness = 0.5) {
    this.doc.moveTo(C.marginX, this.y)
      .lineTo(C.pageW - C.marginX, this.y)
      .strokeColor(color).lineWidth(thickness).stroke();
    this.nl(8);
  }

  // Section heading
  heading(text, opts = {}) {
    const size = opts.size || 11;
    this.doc.fontSize(size).font('Helvetica-Bold')
      .fillColor(opts.color || C.accent)
      .text(text, C.marginX, this.y, { width: bodyW });
    this.y = this.doc.y + 4;
  }

  // Body text
  body(text, opts = {}) {
    this.doc.fontSize(opts.size || 8.5).font(opts.bold ? 'Helvetica-Bold' : 'Helvetica')
      .fillColor(opts.color || C.mid)
      .text(text, opts.x || C.marginX, this.y, { width: opts.width || bodyW, lineGap: 3 });
    this.y = this.doc.y + (opts.gap || 4);
  }

  // Italic caption
  caption(text) {
    this.doc.fontSize(7.5).font('Helvetica-Oblique').fillColor('#718096')
      .text(text, C.marginX, this.y, { width: bodyW });
    this.y = this.doc.y + 4;
  }

  // Table row — cols: [{ text, width, align, bold, color }]
  tableRow(cols, opts = {}) {
    const rowH = opts.rowH || 18;
    const bgColor = opts.bg;
    if (bgColor) {
      this.doc.rect(C.marginX, this.y, bodyW, rowH).fillColor(bgColor).fill();
    }
    let x = C.marginX;
    for (const col of cols) {
      const align = col.align || 'left';
      const color = col.color || C.black;
      this.doc.fontSize(8).font(col.bold ? 'Helvetica-Bold' : 'Helvetica')
        .fillColor(color)
        .text(col.text || '', x + 4, this.y + 4, { width: (col.width || 100) - 8, align });
      x += col.width || 100;
    }
    this.y += rowH;
  }

  // Table header row
  tableHeader(cols) {
    this.tableRow(cols.map(c => ({ ...c, bold: true, color: C.white })), { bg: C.accent, rowH: 20 });
  }

  // Number row with sign-colour
  financialRow(label, value, indent = 0, subtotal = false) {
    const isNeg = typeof value === 'number' && value < 0;
    const display = typeof value === 'number' ? usd(value) : (value || '—');
    const color = typeof value === 'number' ? (value < 0 ? C.neg : C.black) : C.mid;
    this.tableRow([
      { text: ' '.repeat(indent * 2) + label, width: bodyW * 0.7, bold: subtotal },
      { text: display, width: bodyW * 0.3, align: 'right', bold: subtotal, color },
    ], subtotal ? { bg: '#f7fafc' } : {});
  }

  // Page footer
  footer(pageNum, totalPages) {
    const y = C.pageH - 36;
    this.doc.fontSize(7).font('Helvetica').fillColor('#a0aec0')
      .text('Confidential — Generated by MetaGauge | metagauge.io', C.marginX, y, { width: bodyW * 0.7 })
      .text(`Page ${pageNum} of ${totalPages}`, C.pageW - C.marginX - 80, y, { width: 80, align: 'right' });
  }

  // Force new page
  newPage() {
    this.doc.addPage();
    this.y = C.marginY;
  }

  // Check if we need a new page (leave bottom margin)
  checkPage(needed = 60) {
    if (this.y + needed > C.pageH - 80) this.newPage();
  }
}

// ── Page renderers ─────────────────────────────────────────────────────────

function renderCover(b, docs, projectName, period, chain) {
  // Full-page dark background
  b.doc.rect(0, 0, C.pageW, C.pageH).fillColor(C.accent).fill();

  // MetaGauge wordmark
  b.doc.fontSize(13).font('Helvetica-Bold').fillColor(C.white)
    .text('MetaGauge', C.marginX, 90);
  b.doc.fontSize(8).font('Helvetica').fillColor('#a0aec0')
    .text('Financial Intelligence Platform', C.marginX, 108);

  // Divider
  b.doc.moveTo(C.marginX, 130).lineTo(C.pageW - C.marginX, 130)
    .strokeColor('#2d3748').lineWidth(1).stroke();

  // Title
  b.doc.fontSize(26).font('Helvetica-Bold').fillColor(C.white)
    .text('Financial Intelligence Report', C.marginX, 180, { width: bodyW });

  b.doc.fontSize(14).font('Helvetica').fillColor('#a0aec0')
    .text(projectName, C.marginX, 225)
    .text(period + ' · ' + (chain || 'Ethereum'), C.marginX, 245);

  // Confidential badge
  b.doc.rect(C.marginX, 300, 100, 24).fillColor('#c53030').fill();
  b.doc.fontSize(9).font('Helvetica-Bold').fillColor(C.white)
    .text('CONFIDENTIAL', C.marginX + 10, 307);

  // Generated date
  b.doc.fontSize(8).font('Helvetica').fillColor('#718096')
    .text(`Generated: ${new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`, C.marginX, C.pageH - 70);
}

function renderExecutiveSummary(b, docs) {
  b.newPage();
  b.heading('Executive Summary', { size: 14 });
  b.hr();

  if (docs.executive_summary) {
    b.body(docs.executive_summary, { size: 9, gap: 8 });
  }

  b.nl(8);

  // Key highlights box
  b.heading('Key Highlights', { size: 10 });
  b.nl(4);

  const pl = docs.income_statement;
  const cf = docs.cash_flow_statement;
  const ue = docs.unit_economics;
  const kpi = docs.kpi_dashboard;

  const highlights = [
    ['Total Revenue', usd(pl?.revenue?.total_revenue)],
    ['Gross Margin', pct(pl?.gross_margin_pct)],
    ['Net Profit/Loss', usd(pl?.net_profit)],
    ['Monthly Burn', usd(cf?.summary?.monthly_burn_rate)],
    ['Cash Runway', cf?.summary?.runway_months ? `${Math.floor(cf.summary.runway_months)} months` : 'N/A'],
    ['Total Users', (kpi?.kpis?.users?.total || 0).toLocaleString()],
    ['LTV:CAC', ue?.ratios?.ltv_cac_label || 'N/A'],
    ['Retention', pct(kpi?.kpis?.engagement?.retention_rate)],
  ];

  b.tableHeader([
    { text: 'Metric', width: bodyW * 0.5 },
    { text: 'Value', width: bodyW * 0.5 },
  ]);
  let alt = false;
  for (const [metric, value] of highlights) {
    b.tableRow([
      { text: metric, width: bodyW * 0.5 },
      { text: value, width: bodyW * 0.5, bold: true },
    ], alt ? { bg: '#f7fafc' } : {});
    alt = !alt;
  }
}

