function renderForwardModel(b, docs) {
  b.newPage();
  b.heading('12-Month Forward Model', { size: 14 });
  b.caption('Base / Bull / Bear scenarios — for planning purposes only');
  b.hr();

  const fm = docs.forward_model;
  if (!fm) { b.body('No data available.'); return; }

  for (const scenario of [fm.base, fm.bull, fm.bear]) {
    b.checkPage(120);
    b.heading(scenario?.label || 'Scenario', { size: 10 });
    b.body(`Revenue growth: ${scenario?.assumptions?.monthly_revenue_growth || 'N/A'}/mo | Cost growth: ${scenario?.assumptions?.monthly_cost_growth || 'N/A'}/mo | Break-even: Month ${scenario?.break_even_month || 'N/A'}`, { size: 8 });
    b.nl(4);

    const colW = bodyW / 4;
    b.tableHeader([
      { text: 'Month', width: colW * 0.6 },
      { text: 'Revenue', width: colW * 1.1 },
      { text: 'Costs', width: colW * 1.1 },
      { text: 'Profit', width: colW * 1.2 },
    ]);

    let alt = false;
    for (const m of (scenario?.months || [])) {
      const isProfit = (m.projected_profit || 0) >= 0;
      b.tableRow([
        { text: `Month ${m.month}`, width: colW * 0.6 },
        { text: usd(m.projected_revenue), width: colW * 1.1, align: 'right' },
        { text: usd(m.projected_costs), width: colW * 1.1, align: 'right' },
        { text: usd(m.projected_profit), width: colW * 1.2, align: 'right', color: isProfit ? C.pos : C.neg, bold: true },
      ], alt ? { bg: '#f7fafc' } : {});
      alt = !alt;
    }
    b.nl(12);
  }

  if (docs.forward_model?.notes) {
    b.nl(4);
    b.heading('Assumptions & Disclaimer', { size: 9 });
    for (const note of docs.forward_model.notes) {
      b.body('• ' + note, { size: 7.5, gap: 3 });
    }
  }
}

function renderRedFlagsAndQA(b, docs) {
  b.newPage();
  b.heading('Red Flags & Opportunities', { size: 14 });
  b.hr();

  if (docs.red_flags?.length > 0) {
    b.heading('⚠ Red Flags', { size: 10, color: C.neg });
    b.nl(4);
    for (const flag of docs.red_flags) {
      b.body('• ' + flag, { size: 8.5, gap: 5 });
    }
  }

  b.nl(12);

  if (docs.opportunities?.length > 0) {
    b.heading('✓ Opportunities', { size: 10, color: C.pos });
    b.nl(4);
    for (const op of docs.opportunities) {
      b.body('• ' + op, { size: 8.5, gap: 5 });
    }
  }

  if (docs.investor_qa?.length > 0) {
    b.checkPage(120);
    b.newPage();
    b.heading('Investor Q&A Preparation', { size: 14 });
    b.caption('Likely questions from Series A/B investors — with suggested responses');
    b.hr();

    for (const qa of docs.investor_qa) {
      b.checkPage(60);
      b.heading('Q: ' + qa.question, { size: 9, color: C.accent });
      b.body(qa.answer, { size: 8.5, gap: 10 });
      b.hr('#e2e8f0', 0.3);
    }
  }
}

// ── Main export function ───────────────────────────────────────────────────

export async function generateFinancialPDF(docs, meta = {}) {
  const { projectName = 'Unknown Project', contractAddress = '', chain = 'ethereum' } = meta;
  const period = docs.period || 'N/A';

  const filename = `${contractAddress}_${chain}_${period}.pdf`.replace(/[^a-zA-Z0-9._-]/g, '_');
  const outputPath = join(OUTPUT_DIR, filename);
  ensureDir(outputPath);

  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: 0, autoFirstPage: false });
    const stream = createWriteStream(outputPath);

    doc.pipe(stream);

    // We'll track page count manually
    let pageNum = 0;
    doc.on('pageAdded', () => { pageNum++; });

    const b = new PDFBuilder(doc);

    // Page 1 — Cover
    doc.addPage();
    renderCover(b, docs, projectName, period, chain);

    // Page 2 — Executive Summary
    renderExecutiveSummary(b, docs);

    // Page 3 — Income Statement
    renderIncomeStatement(b, docs);

    // Page 4 — Cash Flow
    renderCashFlow(b, docs);

    // Page 5 — Balance Sheet
    renderBalanceSheet(b, docs);

    // Page 6 — Unit Economics
    renderUnitEconomics(b, docs);

    // Page 7 — 12-Month Forward Model
    renderForwardModel(b, docs);

    // Page 8/9 — Red Flags + Investor Q&A
    renderRedFlagsAndQA(b, docs);

    // Finalize
    doc.end();

    stream.on('finish', () => resolve(outputPath));
    stream.on('error', reject);
  });
}
