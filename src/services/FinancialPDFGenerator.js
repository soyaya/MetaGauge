  b.financialRow('Token Revenue', pl.revenue?.token_revenue, 1);
  b.financialRow('Off-chain Revenue', pl.revenue?.off_chain_revenue, 1);
  b.financialRow('Total Revenue', pl.revenue?.total_revenue, 0, true);

  b.nl(6);
  b.heading('Cost of Revenue (COGS)', { size: 9 });
  b.financialRow('Gas Subsidies', pl.cost_of_revenue?.gas_subsidies, 1);
  b.financialRow('Infrastructure', pl.cost_of_revenue?.infrastructure, 1);
  b.financialRow('Total COGS', pl.cost_of_revenue?.total_cogs, 0, true);
  b.financialRow('Gross Profit', pl.gross_profit, 0, true);
  b.financialRow('Gross Margin %', pct(pl.gross_margin_pct), 0);

  b.nl(6);
  b.heading('Operating Expenses', { size: 9 });
  b.financialRow('Marketing & Advertising', pl.operating_expenses?.marketing, 1);
  b.financialRow('Payroll', pl.operating_expenses?.payroll, 1);
